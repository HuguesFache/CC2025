/* Transcode
*/
bool8 Transcoder::Transcode(uchar& theCharacter)
{
	uchar ch;
	
	if(theCharacter < ' ') 
	{
		int16 i = 0;
		i = theCharacter;
		ch = i + 1;
	}
	ch = Code[theCharacter];
	
	if(ch == 0x00) // Don't transcode character
	{	
		return kFalse;
	}
	theCharacter = ch;
	return kTrue;
}